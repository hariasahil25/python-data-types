# Program to demonstrate different data types in Python

# Integer
num = 25

# Float
price = 99.99

# String
name = "Ash King"

# Boolean
is_student = True

# List
subjects = ["Python", "Java", "C++"]

# Tuple
coordinates = (10, 20)

# Dictionary
student = {
    "name": "Ash King",
    "age": 20,
    "course": "BCA"
}

# Set
unique_numbers = {1, 2, 3, 4, 5}

# Printing values and their data types
print("Value:", num, "| Data Type:", type(num))
print("Value:", price, "| Data Type:", type(price))
print("Value:", name, "| Data Type:", type(name))
print("Value:", is_student, "| Data Type:", type(is_student))
print("Value:", subjects, "| Data Type:", type(subjects))
print("Value:", coordinates, "| Data Type:", type(coordinates))
print("Value:", student, "| Data Type:", type(student))
print("Value:", unique_numbers, "| Data Type:", type(unique_numbers))
